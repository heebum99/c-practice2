#include "khb.h"

int main() 
{
	//비트 연산자
	int a = 10; //비트열 00000000 00000000 00000000 00001010
	int b = 12; //비트열 00000000 00000000 00000000 00001100

	printf("a & b : %d\n", a & b); //& 논리곱 연산자 => 00000000 00000000 00000000 00001000
	printf("a ^ b : %d\n", a ^ b); //^ 배타적 논리합 연산자(값이 서로 다르면 1, 같으면 0) => 00000000 00000000 00000000 00000110
	printf("a | b : %d\n", a | b); //| 논리합 연산자 => 00000000 00000000 00000000 00001110
	printf("~a : %d\n", ~a); //~ 부정 연산자 
	//=> 11111111 11111111 11111111 11110101 => -1을 해주면 11111111 11111111 11111111 11110100에서 1의보수를 해주면 00000000 00000000 00000000 00001011 즉 절대값 11이므로 -11이 된다.
	printf("a << 1 : %d\n", a << 1); //00000000 00000000 00000000 00010100 => 20
	printf("a >> 2 : %d\n", a >> 2); //00000000 00000000 00000000 00000010 => 2


	return 0;
}